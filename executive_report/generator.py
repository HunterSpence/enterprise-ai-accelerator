"""
ExecutiveReport — AI-Generated Board-Ready Reporting

Transforms technical analysis output into polished executive reports
formatted for non-technical stakeholders: C-suite, board members,
procurement committees.
"""

import json
import os
from dataclasses import dataclass, field
from datetime import datetime
from typing import Optional
from pathlib import Path

import anthropic
from dotenv import load_dotenv

load_dotenv()


@dataclass
class ExecutiveReport:
    title: str
    company_name: str
    generated_at: str
    executive_summary: str = ""
    key_findings: list[str] = field(default_factory=list)
    financial_impact: dict = field(default_factory=dict)
    risk_matrix: list[dict] = field(default_factory=list)
    recommendations: list[dict] = field(default_factory=list)
    next_steps: list[str] = field(default_factory=list)
    html_content: str = ""
    
    def save_html(self, path: str | Path = None) -> Path:
        output_path = Path(path or f"output/{self.company_name}_report.html")
        output_path.parent.mkdir(exist_ok=True)
        output_path.write_text(self.html_content)
        return output_path
    
    def print_summary(self):
        from rich.console import Console
        from rich.panel import Panel
        console = Console()
        console.print(Panel(self.executive_summary, title=f"[bold]{self.title}", border_style="purple"))
        
        if self.key_findings:
            console.print("\n[bold]Key Findings:")
            for f in self.key_findings:
                console.print(f"  • {f}")
        
        if self.financial_impact:
            console.print(f"\n[bold]Financial Impact:")
            for k, v in self.financial_impact.items():
                console.print(f"  {k}: {v}")


