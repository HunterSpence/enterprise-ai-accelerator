@dataclass
class RunbookStep:
    """A single step in a migration runbook."""
    step_number: int
    title: str
    description: str
    commands: list[str] = field(default_factory=list)
    validation: str = ""
    estimated_minutes: int = 15
    responsible_role: str = "Cloud Engineer"
    is_critical: bool = False


@dataclass
class MigrationRunbook:
    """Complete runbook for migrating a single workload."""
    workload_id: str
    workload_name: str
    strategy: MigrationStrategy
    target_service: str
    pre_migration_checklist: list[str]
    migration_steps: list[RunbookStep]
    validation_tests: list[str]
    rollback_procedure: list[str]
    post_migration_checklist: list[str]
    estimated_total_hours: float
    ai_generated: bool = False
    generated_at: str = ""

    def to_markdown(self) -> str:
        """Render the runbook as Markdown."""
        lines = [
            f"# Migration Runbook: {self.workload_name}",
            "",
            f"> **Strategy:** {self.strategy.value}  ",
            f"> **Target:** {self.target_service}  ",
            f"> **Estimated Duration:** {self.estimated_total_hours:.1f} hours  ",
            f"> **Generated:** {self.generated_at}  ",
            "",
            "---",
            "",
            "## Pre-Migration Checklist",
            "",
        ]

        for i, item in enumerate(self.pre_migration_checklist, 1):
            lines.append(f"- [ ] {item}")

        lines += ["", "---", "", "## Migration Steps", ""]

        for step in self.migration_steps:
            critical_badge = " [CRITICAL]" if step.is_critical else ""
            lines.append(f"### Step {step.step_number}: {step.title}{critical_badge}")
            lines.append(f"**Role:** {step.responsible_role} | **Est. Time:** {step.estimated_minutes} min")
            lines.append("")
            lines.append(step.description)

            if step.commands:
                lines.append("")
                lines.append("```bash")
                for cmd in step.commands:
                    lines.append(cmd)
                lines.append("```")

            if step.validation:
                lines.append("")
                lines.append(f"**Validation:** {step.validation}")

            lines.append("")

        lines += ["---", "", "## Validation Tests", ""]
        for test in self.validation_tests:
            lines.append(f"- [ ] {test}")

        lines += ["", "---", "", "## Cutover Procedure", ""]
        lines.append("1. Confirm all validation tests pass")
        lines.append("2. Schedule cutover window with stakeholders (prefer off-peak hours)")
        lines.append("3. Send cutover notification to dependent teams")
        lines.append("4. Execute DNS/load balancer cutover")
        lines.append("5. Monitor for 30 minutes post-cutover")
        lines.append("6. Send all-clear notification")

        lines += ["", "---", "", "## Rollback Procedure", ""]
        for i, step in enumerate(self.rollback_procedure, 1):
            lines.append(f"{i}. {step}")

        lines += ["", "---", "", "## Post-Migration Checklist", ""]
        for item in self.post_migration_checklist:
            lines.append(f"- [ ] {item}")

        lines += ["", "---", "", "*Runbook generated by MigrationScout v2.0.0*"]

        return "\n".join(lines)


@dataclass
class WaveRunbook:
    """Collection of runbooks for a single migration wave."""
    wave: MigrationWave
    runbooks: list[MigrationRunbook]
    wave_kickoff_checklist: list[str]
    wave_completion_checklist: list[str]

    def to_markdown(self) -> str:
        """Render the wave runbook as Markdown."""
        lines = [
            f"# Wave {self.wave.wave_number + 1} Migration Runbook: {self.wave.name}",
            "",
            f"> **Workloads:** {self.wave.workload_count}  ",
            f"> **Estimated Duration:** {self.wave.estimated_duration_weeks:.1f} weeks  ",
            f"> **Risk Level:** {self.wave.risk_level.upper()}  ",
            f"> **Migration Cost:** ${self.wave.total_migration_cost_usd:,.0f}  ",
            "",
            "---",
            "",
            "## Wave Kickoff Checklist",
            "",
        ]

        for item in self.wave_kickoff_checklist:
            lines.append(f"- [ ] {item}")

        lines += ["", "---", ""]

        for runbook in self.runbooks:
            lines.append(runbook.to_markdown())
            lines.append("")
            lines.append("---")
            lines.append("")

        lines += ["## Wave Completion Checklist", ""]
        for item in self.wave_completion_checklist:
            lines.append(f"- [ ] {item}")

        return "\n".join(lines)


