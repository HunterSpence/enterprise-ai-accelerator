def _build_template_runbook(a: WorkloadAssessment) -> MigrationRunbook:
    """Build a template-based runbook when AI is unavailable."""
    template = _TEMPLATE_STEPS.get(a.strategy, _TEMPLATE_STEPS[MigrationStrategy.REHOST])
    steps: list[RunbookStep] = []

    for i, (title, desc, cmds, mins) in enumerate(template, 1):
        steps.append(RunbookStep(
            step_number=i,
            title=title,
            description=desc,
            commands=cmds,
            estimated_minutes=mins,
            is_critical=(i == 1 or i == len(template)),
        ))

    pre_checklist = [
        "Backup confirmation: full backup completed and verified",
        "Stakeholder notification sent (at least 48hr in advance)",
        "Rollback plan documented and reviewed",
        "Team availability confirmed for migration window",
        "All dependencies identified and owners notified",
        "Monitoring dashboards open and baselines recorded",
        "On-call escalation path documented",
    ]

    validation_tests = [
        f"Application URL returns HTTP 200",
        f"Core workflow end-to-end test passes",
        f"Database connectivity verified",
        f"Performance baseline within 20% of pre-migration",
        f"Security group ingress/egress validated",
        f"Logging and monitoring active in CloudWatch",
        f"Backup policy applied to new resources",
    ]

    rollback = [
        "Assess severity and make go/no-go decision (within 15 minutes of issue detection)",
        "Notify stakeholders of rollback initiation",
        "Revert DNS to point to original on-premises system",
        "Verify on-premises system health and traffic acceptance",
        "Stop cloud resources (do not delete — needed for post-mortem)",
        "Conduct rollback retrospective within 24 hours",
        "Document root cause and update runbook before re-attempt",
    ]

    post_checklist = [
        "Verify all monitoring alerts configured",
        "Confirm backup schedule active",
        "Update CMDB with new cloud resource IDs",
        "Remove on-premises firewall rules pointing to migrated app",
        "Schedule on-premises server decommission (allow 2-week parallel run)",
        "Update DNS TTL to normal (from 60s to 3600s)",
        "Close migration ticket and notify stakeholders",
    ]

    return MigrationRunbook(
        workload_id=a.workload.id,
        workload_name=a.workload.name,
        strategy=a.strategy,
        target_service=a.target_service,
        pre_migration_checklist=pre_checklist,
        migration_steps=steps,
        validation_tests=validation_tests,
        rollback_procedure=rollback,
        post_migration_checklist=post_checklist,
        estimated_total_hours=sum(s.estimated_minutes for s in steps) / 60.0,
        ai_generated=False,
        generated_at=datetime.now().strftime("%Y-%m-%d %H:%M UTC"),
    )


def _parse_ai_runbook(raw_text: str, a: WorkloadAssessment) -> MigrationRunbook:
    """Parse AI-generated runbook text into structured MigrationRunbook."""
    # Use the AI text as-is for the migration steps description
    # Parse sections by heading markers
    sections: dict[str, list[str]] = {}
    current_section = "intro"
    sections[current_section] = []

    for line in raw_text.split("\n"):
        if line.startswith("##"):
            current_section = line.lstrip("#").strip().lower()
            sections[current_section] = []
        else:
            sections.setdefault(current_section, []).append(line)

    def extract_checklist(section_key: str) -> list[str]:
        items = []
        for line in sections.get(section_key, []):
            line = line.strip()
            if line.startswith(("- ", "* ", "[ ] ")):
                item = line.lstrip("-*[ ]").strip()
                if item:
                    items.append(item)
            elif line and line[0].isdigit() and "." in line[:3]:
                item = line.split(".", 1)[-1].strip()
                if item:
                    items.append(item)
        return items or [f"Refer to AI-generated runbook for {section_key} items"]

    # Build steps from AI output
    migration_text = "\n".join(
        sections.get("migration steps", [])
        or sections.get("migration execution steps", [])
        or sections.get("steps", [])
    )

    steps: list[RunbookStep] = []
    current_step_lines: list[str] = []
    step_num = 1

    for line in migration_text.split("\n"):
        if line.strip().startswith(("### Step", "**Step ")):
            if current_step_lines:
                steps.append(RunbookStep(
                    step_number=step_num - 1,
                    title=f"Migration Step {step_num - 1}",
                    description="\n".join(current_step_lines[:3]),
                    commands=[l.strip().lstrip("- ") for l in current_step_lines if l.strip().startswith("`")],
                    estimated_minutes=30,
                ))
                current_step_lines = []
        elif line.strip():
            current_step_lines.append(line.strip())
            step_num += 1

    if not steps:
        # Fall back to a single step with all the AI content
        steps = [RunbookStep(
            step_number=1,
            title="AI-Generated Migration Steps",
            description=migration_text[:1000] if migration_text else "See full AI runbook content.",
            estimated_minutes=120,
            is_critical=True,
        )]

    pre = extract_checklist("pre-migration checklist")
    validation = extract_checklist("validation tests")
    rollback = extract_checklist("rollback procedure")
    post = extract_checklist("post-migration checklist")

    total_hours = sum(s.estimated_minutes for s in steps) / 60.0 or 4.0

    return MigrationRunbook(
        workload_id=a.workload.id,
        workload_name=a.workload.name,
        strategy=a.strategy,
        target_service=a.target_service,
        pre_migration_checklist=pre or ["Backup completed", "Team notified", "Rollback plan ready"],
        migration_steps=steps,
        validation_tests=validation or ["Application health check passes", "Core workflows validated"],
        rollback_procedure=rollback or ["Revert DNS", "Restore from backup", "Notify stakeholders"],
        post_migration_checklist=post or ["Update CMDB", "Configure monitoring", "Decommission source"],
        estimated_total_hours=total_hours,
        ai_generated=True,
        generated_at=datetime.now().strftime("%Y-%m-%d %H:%M UTC"),
    )


@dataclass
class RunbookResult:
    """Lightweight result struct for API usage (POST /runbooks/{id})."""
    content: str
    estimated_hours: float
    risk_level: str
    ai_generated: bool


def _risk_level_from_assessment(a: WorkloadAssessment) -> str:
    """Map risk_score (0-100) to low/medium/high/critical."""
    score = getattr(a, "risk_score", 50)
    if score >= 75:
        return "critical"
    if score >= 55:
        return "high"
    if score >= 35:
        return "medium"
    return "low"


class RunbookGenerator:
    """
    Generates migration runbooks using Claude Sonnet.
    Falls back to template-based generation when AI is unavailable.
    """

    def __init__(self, use_ai: bool = True) -> None:
        self.use_ai = use_ai
        self._client: anthropic.Anthropic | None = None

        if self.use_ai:
            api_key = os.environ.get("ANTHROPIC_API_KEY")
            if api_key:
                self._client = anthropic.Anthropic(api_key=api_key)
            else:
                console.print("[yellow]ANTHROPIC_API_KEY not set — using template-based runbooks[/yellow]")
                self.use_ai = False

    def _ai_generate_runbook(self, a: WorkloadAssessment) -> MigrationRunbook:
        """Call Claude Sonnet to generate a detailed, workload-specific runbook."""
        if not self._client:
            return _build_template_runbook(a)

        prompt = f"""You are a senior cloud migration architect writing a detailed migration runbook.

WORKLOAD: {a.workload.name}
Type: {a.workload.workload_type}
Stack: {a.workload.language}/{a.workload.framework} on {a.workload.os}
Migration Strategy: {a.strategy.value}
Target Service: {a.target_service}
Complexity: {a.complexity.value}
Risk Score: {a.risk_score}/100
Business Criticality: {a.workload.business_criticality}
Notes: {a.workload.notes or 'None'}
AI Assessment: {a.ai_rationale}

Write a complete, detailed migration runbook with these EXACT sections:

## Pre-Migration Checklist
(10-12 items as bullet points - [ ] format)

## Migration Execution Steps
(5-7 numbered steps, each with shell commands where applicable)

## Validation Tests
(8-10 items as bullet points - [ ] format)

## Rollback Procedure
(6-8 steps as numbered list)

## Post-Migration Checklist
(8-10 items as bullet points - [ ] format)

Be specific to this workload type and strategy. Include real AWS CLI commands where applicable.
Keep each section concise but actionable. Total response should be ~800 words."""

        try:
            response = self._client.messages.create(
                model="claude-sonnet-4-6",
                max_tokens=2048,
                messages=[{"role": "user", "content": prompt}],
            )
            raw_text = response.content[0].text
            return _parse_ai_runbook(raw_text, a)
        except Exception as exc:
            console.print(f"[yellow]AI runbook failed for {a.workload.name}: {exc} — using template[/yellow]")
            return _build_template_runbook(a)

    def generate_runbook(self, a: WorkloadAssessment) -> MigrationRunbook:
        """Generate a runbook for a single workload."""
        if self.use_ai:
            return self._ai_generate_runbook(a)
        return _build_template_runbook(a)

    def generate_workload_runbook(self, a: WorkloadAssessment) -> "RunbookResult":
        """
        Generate a runbook for API usage. Returns a simple RunbookResult.
        Used by api.py POST /runbooks/{workload_id}.
        """
        runbook = self.generate_runbook(a)
        return RunbookResult(
            content=runbook.to_markdown(),
            estimated_hours=runbook.estimated_total_hours,
            risk_level=_risk_level_from_assessment(a),
            ai_generated=runbook.ai_generated,
        )

    def generate_wave_runbook(
        self,
        wave: MigrationWave,
        assessments: list[WorkloadAssessment],
        show_progress: bool = True,
    ) -> WaveRunbook:
        """Generate runbooks for all workloads in a wave."""
        wave_assessments = [a for a in assessments if a.workload.id in wave.workload_ids]
        runbooks: list[MigrationRunbook] = []

        if show_progress:
            with Progress(
                SpinnerColumn(),
                TextColumn(f"[bold blue]Generating Wave {wave.wave_number + 1} runbooks..."),
                BarColumn(),
                TaskProgressColumn(),
                console=console,
            ) as progress:
                task = progress.add_task("", total=len(wave_assessments))
                for a in wave_assessments:
                    runbooks.append(self.generate_runbook(a))
                    progress.advance(task)
        else:
            runbooks = [self.generate_runbook(a) for a in wave_assessments]

        kickoff_checklist = [
            f"Wave {wave.wave_number + 1} go/no-go meeting completed",
            "All wave workload owners confirmed available",
            "Rollback decision criteria documented and agreed",
            "Monitoring dashboards configured for all wave workloads",
            "Communication channel (Slack/Teams) created for wave team",
            "Cloud resources pre-provisioned and validated",
            "Network connectivity tests passed (VPN/Direct Connect)",
            "Change management ticket approved",
            f"Wave budget confirmed: ${wave.total_migration_cost_usd:,.0f}",
        ]

        completion_checklist = [
            f"All {wave.workload_count} workloads migrated and validated",
            "Source systems operating in read-only parallel mode",
            "Monitoring baselines established for all migrated workloads",
            "Cost tracking tags applied to all new cloud resources",
            "Wave retrospective meeting scheduled",
            f"Monthly savings target validated: ${wave.total_monthly_savings_usd:,.0f}/mo",
            "Next wave kickoff scheduled",
            "Wave completion report sent to stakeholders",
        ]

        return WaveRunbook(
            wave=wave,
            runbooks=runbooks,
            wave_kickoff_checklist=kickoff_checklist,
            wave_completion_checklist=completion_checklist,
        )

    def generate_all_wave_runbooks(
        self,
        plan: WavePlan,
        assessments: list[WorkloadAssessment],
        output_dir: str | None = None,
        show_progress: bool = True,
    ) -> list[WaveRunbook]:
        """
        Generate runbooks for all waves in a plan.

        If output_dir is provided, exports Markdown files to that directory.
        """
        wave_runbooks: list[WaveRunbook] = []

        console.print(f"\n[bold blue]Generating migration runbooks for {len(plan.waves)} waves...[/bold blue]")
        if self.use_ai:
            console.print("[dim]Using Claude Sonnet for AI-powered runbook generation[/dim]")
        else:
            console.print("[dim]Using template-based runbook generation[/dim]")

        for wave in plan.waves:
            console.print(f"\n[bold]Wave {wave.wave_number + 1}: {wave.name}[/bold] ({wave.workload_count} workloads)")
            wave_runbook = self.generate_wave_runbook(wave, assessments, show_progress=show_progress)
            wave_runbooks.append(wave_runbook)

        if output_dir:
            self._export_runbooks(wave_runbooks, output_dir)

        total_runbooks = sum(len(wr.runbooks) for wr in wave_runbooks)
        console.print(
            Panel(
                f"  Runbooks generated:   [bold white]{total_runbooks}[/bold white]\n"
                f"  Waves covered:        [bold white]{len(wave_runbooks)}[/bold white]\n"
                f"  AI-generated:         [bold cyan]{'Yes' if self.use_ai else 'No (template-based)'}[/bold cyan]"
                + (f"\n  Output directory:     [bold green]{output_dir}[/bold green]" if output_dir else ""),
                title="[bold]Runbook Generation Complete[/bold]",
                border_style="green",
            )
        )

        return wave_runbooks

    def _export_runbooks(self, wave_runbooks: list[WaveRunbook], output_dir: str) -> None:
        """Export all runbooks as Markdown files."""
        output_path = Path(output_dir)
        output_path.mkdir(parents=True, exist_ok=True)

        for wr in wave_runbooks:
            filename = f"wave_{wr.wave.wave_number + 1:02d}_{wr.wave.name.lower().replace(' ', '_')}_runbook.md"
            file_path = output_path / filename
            with open(file_path, "w", encoding="utf-8") as f:
                f.write(wr.to_markdown())
            console.print(f"  [green]Exported:[/green] {file_path}")
